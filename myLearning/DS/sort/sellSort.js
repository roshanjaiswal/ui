/* THE SHELLSORT ALGORITHM 
This algorithm is fundamentally an improvement of the insertion sort. 
The key concept in this algorithm is that it compares items that are distant rather than adjacent items, 
as is done in the insertion sort. 
As the algorithm loops through the data set, 
the distance between each item decreases until at the end the algorithm is comparing items that are adjacent.

ShellSort sorts distant elements by using an increment sequence. 
The sequence must start with 1, but can then be incremented by any amount.
*/